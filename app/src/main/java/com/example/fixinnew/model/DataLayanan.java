package com.example.fixinnew.model;

public class DataLayanan {

    int id_layanan, idbengkel, harga_layanan;
    String deskripsi_layanan, alamat, nama_layanan;

    public int getId_layanan() {
        return id_layanan;
    }

    public void setId_layanan(int id_layanan) {
        this.id_layanan = id_layanan;
    }

    public int getIdbengkel() {
        return idbengkel;
    }

    public void setIdbengkel(int idbengkel) {
        this.idbengkel = idbengkel;
    }

    public int getHarga_layanan() {
        return harga_layanan;
    }

    public void setHarga_layanan(int harga_layanan) {
        this.harga_layanan = harga_layanan;
    }

    public String getDeskripsi_layanan() {
        return deskripsi_layanan;
    }

    public void setDeskripsi_layanan(String deskripsi_layanan) {
        this.deskripsi_layanan = deskripsi_layanan;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getNama_layanan() {
        return nama_layanan;
    }

    public void setNama_layanan(String nama_layanan) {
        this.nama_layanan = nama_layanan;
    }
}
